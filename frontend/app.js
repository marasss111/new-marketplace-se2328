let provider, signer, tokenContract, marketplaceContract;

// Connect Metamask
document.getElementById("connectWallet").addEventListener("click", async () => {
    if (window.ethereum) {
        provider = new ethers.BrowserProvider(window.ethereum);
        signer = await provider.getSigner();
        document.getElementById("walletAddress").textContent = `Wallet: ${await signer.getAddress()}`;

        // Load Contracts
        tokenContract = new ethers.Contract(tokenAddress, tokenABI, signer);
        marketplaceContract = new ethers.Contract(marketplaceAddress, marketplaceABI, signer);

        // Fetch Token Balance
        fetchBalance();
        fetchModels();
    } else {
        alert("Please install Metamask!");
    }
});

// Fetch Token Balance
async function fetchBalance() {
    const balance = await tokenContract.balanceOf(await signer.getAddress());
    document.getElementById("balance").textContent = ethers.formatUnits(balance, 18);
}

// List AI Model
document.getElementById("listModel").addEventListener("click", async () => {
    const name = document.getElementById("modelName").value;
    const description = document.getElementById("modelDesc").value;
    const price = document.getElementById("modelPrice").value;

    if (!name || !description || price <= 0) {
        alert("Please fill all fields!");
        return;
    }

    const tx = await marketplaceContract.listModel(name, description, ethers.parseUnits(price, 18));
    await tx.wait();

    alert("AI Model listed successfully!");
    fetchModels();
});

// Fetch Listed Models
async function fetchModels() {
    const models = await marketplaceContract.getModels();
    const modelList = document.getElementById("modelList");
    modelList.innerHTML = "";

    models.forEach((model, index) => {
        if (!model.sold) {
            const div = document.createElement("div");
            div.innerHTML = `
                <p><strong>${model.name}</strong> - ${model.description}</p>
                <p>Price: ${ethers.formatUnits(model.price, 18)} AITU</p>
                <button onclick="buyModel(${index})">Buy</button>
                <hr>
            `;
            modelList.appendChild(div);
        }
    });
}

// Buy AI Model
async function buyModel(modelId) {
    const model = (await marketplaceContract.getModels())[modelId];

    const txApprove = await tokenContract.approve(marketplaceAddress, model.price);
    await txApprove.wait();

    const txBuy = await marketplaceContract.buyModel(modelId);
    await txBuy.wait();

    alert("Purchase Successful!");
    fetchModels();
}
